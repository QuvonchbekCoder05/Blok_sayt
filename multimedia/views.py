from rest_framework.views import APIView
from rest_framework.response import Response
from multimedia.models.video import Video
from multimedia.models.audio import Audio
from multimedia.models.image import Image
from multimedia.serializers.video_serializer import VideoSerializer
from multimedia.serializers.audio_serializer import AudioSerializer
from multimedia.serializers.image_serializer import ImageSerializer
from rest_framework import status

class VideoView(APIView):

    def get(self, request):
        videos = Video.objects.all()
        serializer = VideoSerializer(videos, many=True)
        return Response(serializer.data)

    def post(self, request):
        serializer = VideoSerializer(data=request.data)
        if serializer.is_valid():
            serializer.save()
            return Response(serializer.data, status=status.HTTP_201_CREATED)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)

